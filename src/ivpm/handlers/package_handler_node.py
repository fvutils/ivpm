_NODE_SENTINEL_BEGIN = "# --- ivpm:node begin ---"
_NODE_SENTINEL_END   = "# --- ivpm:node end ---"


def _write_node_envrc(node_dir: str):
    """Write activation helpers for the Node environment.

    Writes ``packages/node/export.envrc`` (direnv snippet) for Unix/direnv.
    On Windows also writes ``.bat`` and ``.ps1`` activation scripts.
    """
    if not os.path.isdir(node_dir):
        return

    envrc_path = os.path.join(node_dir, "export.envrc")
    with open(envrc_path, "w") as fp:
        fp.write("# Generated by IVPM node handler -- do not edit manually\n")
        fp.write("PATH_add node_modules/.bin\n")
        fp.write("export NODE_PATH=\"$PWD/node_modules\"\n")

    if platform.system() == "Windows":
        bat_path = os.path.join(node_dir, "activate_node.bat")
        with open(bat_path, "w") as fp:
            fp.write("@echo off\r\n")
            fp.write("REM Generated by IVPM node handler -- do not edit manually\r\n")
            fp.write("set \"PATH=%~dp0node_modules\\.bin;%PATH%\"\r\n")
            fp.write("set \"NODE_PATH=%~dp0node_modules\"\r\n")

        ps1_path = os.path.join(node_dir, "activate_node.ps1")
        with open(ps1_path, "w") as fp:
            fp.write("# Generated by IVPM node handler -- do not edit manually\n")
            fp.write("$env:PATH = \"$PSScriptRoot\\node_modules\\.bin;$env:PATH\"\n")
            fp.write("$env:NODE_PATH = \"$PSScriptRoot\\node_modules\"\n")


def _patch_packages_envrc_node(deps_dir: str):
    """Insert or replace the node PATH/NODE_PATH section in packages activation files."""
    envrc_path = os.path.join(deps_dir, "packages.envrc")

    new_section = (
        "%s\n"
        "source_env ./node/export.envrc\n"
        "%s\n"
    ) % (_NODE_SENTINEL_BEGIN, _NODE_SENTINEL_END)

    if not os.path.isfile(envrc_path):
        with open(envrc_path, "w") as fp:
            fp.write("# Generated by IVPM -- do not edit manually\n")
            fp.write(new_section)
    else:
        with open(envrc_path) as fp:
            content = fp.read()

        begin_idx = content.find(_NODE_SENTINEL_BEGIN)
        end_idx   = content.find(_NODE_SENTINEL_END)

        if begin_idx != -1 and end_idx != -1:
            end_of_line = content.find("\n", end_idx)
            end_of_line = len(content) if end_of_line == -1 else end_of_line + 1
            new_content = content[:begin_idx] + new_section + content[end_of_line:]
        else:
            new_content = content + "\n" + new_section

        with open(envrc_path, "w") as fp:
            fp.write(new_content)

    if platform.system() == "Windows":
        bat_path = os.path.join(deps_dir, "packages_activate.bat")
        activate_line = "call \"%~dp0node\\activate_node.bat\"\r\n"
        if os.path.isfile(bat_path):
            with open(bat_path) as fp:
                content = fp.read()
            if "activate_node.bat" not in content:
                with open(bat_path, "a") as fp:
                    fp.write(activate_line)
        else:
            with open(bat_path, "w") as fp:
                fp.write("@echo off\r\n")
                fp.write("REM Generated by IVPM -- do not edit manually\r\n")
                fp.write(activate_line)

        ps1_path = os.path.join(deps_dir, "packages_activate.ps1")
        activate_ps1 = ". \"$PSScriptRoot\\node\\activate_node.ps1\"\n"
        if os.path.isfile(ps1_path):
            with open(ps1_path) as fp:
                content = fp.read()
            if "activate_node.ps1" not in content:
                with open(ps1_path, "a") as fp:
                    fp.write(activate_ps1)
        else:
            with open(ps1_path, "w") as fp:
                fp.write("# Generated by IVPM -- do not edit manually\n")
                fp.write(activate_ps1)


def _is_gitignored(path: str) -> bool:
    """Return True if *path* is gitignored (uses git check-ignore)."""
    try:
        result = subprocess.run(
            ["git", "check-ignore", "-q", path],
            capture_output=True,
        )
        return result.returncode == 0
    except Exception:
        return False


def _package_json_hash(path: str) -> str:
    """Return a hex digest of the contents of a package.json file, or '' on error."""
    try:
        with open(path, "rb") as fp:
            return hashlib.sha256(fp.read()).hexdigest()
    except Exception:
        return ""


@dc.dataclass
class PackageHandlerNode(PackageHandler):
    name:               ClassVar[str]            = "node"
    description:        ClassVar[str]            = "Installs Node.js packages into the managed node environment"
    leaf_when:          ClassVar[Optional[List]] = None
    root_when:          ClassVar[Optional[List]] = None
    phase:              ClassVar[int]            = 6   # after python (5), before fusesoc (10)
    conditions_summary: ClassVar[str]            = ("leaf: all packages; "
                                                     "root: always (skips if no Node packages and no node config)")

    _npm_pkgs:           Dict[str, object] = dc.field(default_factory=dict)
    _source_pkgs:        Dict[str, object] = dc.field(default_factory=dict)
    _explicit_names:     set               = dc.field(default_factory=set)
    _prev_pkg_json_hash: str               = dc.field(default="")
    _current_pkg_json_hash: str            = dc.field(default="")

    def reset(self):
        self._npm_pkgs = {}
        self._source_pkgs = {}
        self._explicit_names = set()
        self._prev_pkg_json_hash = ""
        self._current_pkg_json_hash = ""

    @classmethod
    def handler_info(cls):
        from ..show.info_types import HandlerInfo, ParamInfo
        return HandlerInfo(
            name=cls.name,
            description=cls.description,
            phase=cls.phase,
            conditions=cls.conditions_summary,
            params=[
                ParamInfo("src: npm", "Install a package from the npm registry"),
                ParamInfo("src: package.json", "Import npm deps from an existing package.json"),
                ParamInfo("type: node", "Link a source package into the node environment"),
            ],
        )

    # ------------------------------------------------------------------
    # Leaf callbacks
    # ------------------------------------------------------------------

    def on_leaf_post_load(self, pkg: Package, update_info: ProjectUpdateInfo):
        from ..pkg_types.package_npm import PackageNpm
        from ..pkg_types.package_packagejson import PackagePackageJson

        if pkg.src_type == "npm":
            with self._lock:
                self._npm_pkgs[pkg.name] = pkg
                self._explicit_names.add(pkg.name)
            return

        node_data = get_type_data(pkg, NodeTypeData)
        if node_data is not None:
            with self._lock:
                self._source_pkgs[pkg.name] = (pkg, node_data)
                self._explicit_names.add(pkg.name)
            return

        if pkg.src_type == "package.json":
            self._harvest_packagejson_pkg(pkg, update_info)
            return

        # Auto-detection: source packages (git/dir/http) without an explicit type
        # that contain a package.json are treated as node source packages.
        if (pkg.src_type in ("git", "dir", "http", "gh-rls") and
                getattr(pkg, "pkg_type", None) is None and
                hasattr(pkg, "path") and pkg.path):
            if os.path.isfile(os.path.join(pkg.path, "package.json")):
                td = NodeTypeData()  # defaults: dev=False, link=True
                with self._lock:
                    self._source_pkgs[pkg.name] = (pkg, td)

    def _harvest_packagejson_pkg(self, pkg, update_info: ProjectUpdateInfo):
        """Read a package.json URL and synthesize PackageNpm entries."""
        from ..pkg_types.package_npm import PackageNpm

        url = getattr(pkg, "url", None)
        if not url:
            _logger.warning("src: package.json entry '%s' has no url — skipping", pkg.name)
            return

        # Resolve file:// URLs and ${VAR} substitutions
        path = url
        if path.startswith("file://"):
            path = path[len("file://"):]
        # Expand environment variables
        path = os.path.expandvars(path)

        if not os.path.isfile(path):
            _logger.warning("package.json not found at '%s' — skipping", path)
            return

        try:
            with open(path) as fp:
                data = json.load(fp)
        except Exception as e:
            _logger.warning("Could not read package.json at '%s': %s", path, e)
            return

        with self._lock:
            for dep_name, version in data.get("dependencies", {}).items():
                if dep_name not in self._explicit_names:
                    synth = PackageNpm(dep_name)
                    synth.src_type = "npm"
                    synth.version = version
                    synth.dev = False
                    self._npm_pkgs.setdefault(dep_name, synth)

            for dep_name, version in data.get("devDependencies", {}).items():
                if dep_name not in self._explicit_names:
                    synth = PackageNpm(dep_name)
                    synth.src_type = "npm"
                    synth.version = version
                    synth.dev = True
                    self._npm_pkgs.setdefault(dep_name, synth)

    # ------------------------------------------------------------------
    # Root callbacks
    # ------------------------------------------------------------------

    def on_root_pre_load(self, update_info: ProjectUpdateInfo):
        self.reset()
        # Load previous handler state (hash of last generated package.json)
        state = update_info.handler_state.get("node", {})
        self._prev_pkg_json_hash = state.get("package_json_hash", "")

    def on_root_post_load(self, update_info: ProjectUpdateInfo):
        has_node_pkgs = bool(self._npm_pkgs or self._source_pkgs)
        node_config = getattr(update_info, "node_config", None)

        if not has_node_pkgs and node_config is None:
            return

        # Determine package manager
        manager = "npm"
        if node_config is not None and node_config.manager:
            manager = node_config.manager

        deps_dir = update_info.deps_dir
        node_dir = os.path.join(deps_dir, "node")
        os.makedirs(node_dir, exist_ok=True)

        # Write .nvmrc if version is specified
        if node_config is not None and node_config.version:
            nvmrc_path = os.path.join(node_dir, ".nvmrc")
            with open(nvmrc_path, "w") as fp:
                fp.write(node_config.version + "\n")

        # Generate packages/node/package.json
        pkg_json_path = os.path.join(node_dir, "package.json")
        self._write_package_json(pkg_json_path)

        # Compare hash — skip npm install if package.json unchanged and node_modules exists
        new_hash = _package_json_hash(pkg_json_path)
        node_modules_dir = os.path.join(node_dir, "node_modules")
        install_needed = (
            new_hash != self._prev_pkg_json_hash
            or not os.path.isdir(node_modules_dir)
        )
        self._current_pkg_json_hash = new_hash

        # Run install (only when needed)
        if install_needed:
            with self.task_context(update_info, "node-install", "Installing Node.js packages") as task:
                self._run_install(manager, node_dir, update_info)
        else:
            note("Node packages unchanged — skipping npm install")

        # npm link for source packages
        for pkg_name, (pkg, node_data) in self._source_pkgs.items():
            if node_data.link and hasattr(pkg, "path") and pkg.path:
                with self.task_context(update_info, "node-link-%s" % pkg_name,
                                       "Linking %s" % pkg_name):
                    self._run_link(manager, pkg.path, node_dir, update_info)

        # Patch packages.envrc
        emit_env = node_config.env if node_config is not None else True
        if emit_env:
            _write_node_envrc(node_dir)
            _patch_packages_envrc_node(deps_dir)

        # Warn if lockfile is gitignored
        lockfile = os.path.join(node_dir, "package-lock.json")
        if manager == "npm" and os.path.isfile(lockfile) and _is_gitignored(lockfile):
            note("WARNING: packages/node/package-lock.json is gitignored. "
                 "Consider committing it for reproducible builds.")

    def _write_package_json(self, path: str):
        """Synthesise packages/node/package.json from collected packages."""
        deps = {}
        dev_deps = {}
        for name, pkg in self._npm_pkgs.items():
            if pkg.dev:
                dev_deps[name] = pkg.version
            else:
                deps[name] = pkg.version

        data = {
            "name": "ivpm-node-env",
            "version": "1.0.0",
            "private": True,
        }
        if deps:
            data["dependencies"] = deps
        if dev_deps:
            data["devDependencies"] = dev_deps

        with open(path, "w") as fp:
            json.dump(data, fp, indent=2)
            fp.write("\n")

    def _run_install(self, manager: str, node_dir: str, update_info: ProjectUpdateInfo):
        """Run the appropriate package manager install command."""
        suppress = getattr(update_info, "suppress_output", False)
        if manager == "npm":
            cmd = ["npm", "install", "--prefix", node_dir]
        elif manager == "pnpm":
            cmd = ["pnpm", "install", "--dir", node_dir]
        elif manager == "yarn":
            cmd = ["yarn", "install", "--cwd", node_dir]
        else:
            fatal("Unknown node package manager: %s" % manager)

        _logger.info("Running: %s", " ".join(cmd))
        kwargs = {}
        if suppress:
            kwargs["stdout"] = subprocess.DEVNULL
            kwargs["stderr"] = subprocess.DEVNULL
        try:
            subprocess.run(cmd, check=True, **kwargs)
        except subprocess.CalledProcessError as e:
            fatal("Node package install failed (exit %d): %s" % (e.returncode, " ".join(cmd)))
        except FileNotFoundError:
            fatal("'%s' not found — please install Node.js and %s" % (manager, manager))

    def _run_link(self, manager: str, pkg_path: str, node_dir: str, update_info: ProjectUpdateInfo):
        """npm link a source package into the node environment."""
        suppress = getattr(update_info, "suppress_output", False)
        if manager == "npm":
            cmd = ["npm", "link", pkg_path, "--prefix", node_dir]
        elif manager == "pnpm":
            cmd = ["pnpm", "link", pkg_path, "--dir", node_dir]
        elif manager == "yarn":
            cmd = ["yarn", "link", "--cwd", node_dir, pkg_path]
        else:
            return

        _logger.info("Running: %s", " ".join(cmd))
        kwargs = {}
        if suppress:
            kwargs["stdout"] = subprocess.DEVNULL
            kwargs["stderr"] = subprocess.DEVNULL
        try:
            subprocess.run(cmd, check=True, **kwargs)
        except subprocess.CalledProcessError as e:
            _logger.warning("npm link failed for '%s' (exit %d)", pkg_path, e.returncode)
        except FileNotFoundError:
            _logger.warning("'%s' not found — cannot link '%s'", manager, pkg_path)

    # ------------------------------------------------------------------
    # State persistence
    # ------------------------------------------------------------------

    def get_state_entries(self) -> dict:
        installed = list(self._npm_pkgs.keys()) + list(self._source_pkgs.keys())
        return {
            "manager": "npm",
            "installed": sorted(installed),
            "package_json_hash": self._current_pkg_json_hash,
        }
